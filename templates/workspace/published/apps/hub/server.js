const server = http.createServer(async (req, res) => {
  const url = new URL(req.url, `http://localhost:${PORT}`);
  const p = url.pathname.replace(/\/+$/, '') || '/';

  // CORS
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'GET, POST, DELETE, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
  if (req.method === 'OPTIONS') { res.writeHead(204); return res.end(); }

  try {
    // ── Dashboard ──
    if (p === '' || p === '/hub' || p === '/') {
      res.writeHead(200, { 'Content-Type': 'text/html; charset=utf-8' });
      return res.end(dashboardHTML());
    }

    // ── API: manifest ──
    if (p === '/hub/api/manifest' || p === '/api/manifest') {
      return json(res, readManifest());
    }

    // ── API: health ──
    if (p === '/hub/api/health' || p === '/api/health') {
      const m = readManifest();
      const sites = m.sites.map(checkSiteExists);
      const apps = await Promise.all(m.apps.map(checkAppHealth));
      return json(res, { sites, apps });
    }

    // ── API: stats ──
    if (p === '/hub/api/stats' || p === '/api/stats') {
      return json(res, await getRecentStats());
    }

    // ── API: sync ──
    if ((p === '/hub/api/sync' || p === '/api/sync') && req.method === 'POST') {
      const results = await syncAll();
      return json(res, { ok: true, results });
    }

    // ── API: nginx reload ──
    if ((p === '/hub/api/nginx/reload' || p === '/api/nginx/reload') && req.method === 'POST') {
      const result = await deployNginx(readManifest());
      return json(res, result);
    }

    // ── API: register app (POST) ──
    if ((p === '/hub/api/apps' || p === '/api/apps') && req.method === 'POST') {
      const body = await readBody(req);
      if (!body?.name || !body?.port) return json(res, { error: 'name and port required' }, 400);

      const m = readManifest();
      const entry = {
        name: body.name,
        port: body.port,
        path: body.path || `/${body.name}/`,
        description: body.description || '',
        access: body.access || 'public',
        health_path: body.health_path || '/',
        runtime: body.runtime || 'node',
        exec: body.exec || undefined,
        working_dir: body.working_dir || path.join(PUBLISHED_DIR, 'apps', body.name),
        managed: body.managed !== false,
        created_at: new Date().toISOString(),
      };

      const existing = m.apps.findIndex(a => a.name === body.name);
      if (existing >= 0) m.apps[existing] = { ...m.apps[existing], ...entry };
      else m.apps.push(entry);
      writeManifest(m);

      // Create app directory if needed
      if (!fs.existsSync(entry.working_dir)) {
        fs.mkdirSync(entry.working_dir, { recursive: true });
      }

      // Full deploy: systemd + nginx
      const deploy = await fullDeploy(entry, m);
      return json(res, { ok: deploy.ok, app: entry, steps: deploy.steps }, deploy.ok ? 201 : 500);
    }

    // ── API: register site (POST) ──
    if ((p === '/hub/api/sites' || p === '/api/sites') && req.method === 'POST') {
      const body = await readBody(req);
      if (!body?.name) return json(res, { error: 'name required' }, 400);

      const m = readManifest();
      const entry = {
        name: body.name,
        description: body.description || '',
        access: body.access || 'public',
        created_at: new Date().toISOString(),
      };

      const existing = m.sites.findIndex(s => s.name === body.name);
      if (existing >= 0) m.sites[existing] = { ...m.sites[existing], ...entry };
      else m.sites.push(entry);
      writeManifest(m);

      const dir = path.join(PUBLISHED_DIR, 'sites', body.name);
      if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });

      return json(res, { ok: true, site: entry }, 201);
    }

    // ── API: app actions (start/stop/restart/logs) ──
    const actionMatch = p.match(/^\/(?:hub\/)?api\/apps\/([^/]+)\/(start|stop|restart|logs)$/);
    if (actionMatch) {
      const [, appName, action] = actionMatch;

      if (action === 'logs') {
        const lines = parseInt(url.searchParams.get('lines') || '80');
        const logs = await getServiceLogs(appName, lines);
        return json(res, { logs });
      }

      const result = await serviceAction(appName, action);
      return json(res, result);
    }

    // ── API: delete app ──
    const delAppMatch = p.match(/^\/(?:hub\/)?api\/apps\/([^/]+)$/);
    if (delAppMatch && req.method === 'DELETE') {
      const name = delAppMatch[1];
      const m = readManifest();

      // Remove service
      const app = m.apps.find(a => a.name === name);
      if (app && app.managed !== false) {
        await removeService(name);
      }

      m.apps = m.apps.filter(a => a.name !== name);
      writeManifest(m);

      // Redeploy nginx
      await deployNginx(m);

      return json(res, { ok: true, deleted: name });
    }

    // ── API: delete site ──
    const delSiteMatch = p.match(/^\/(?:hub\/)?api\/sites\/([^/]+)$/);
    if (delSiteMatch && req.method === 'DELETE') {
      const name = delSiteMatch[1];
      const m = readManifest();
      m.sites = m.sites.filter(s => s.name !== name);
      writeManifest(m);
      return json(res, { ok: true, deleted: name });
    }

    json(res, { error: 'not found' }, 404);
  } catch (err) {
    console.error('[Hub Error]', err);
    json(res, { error: err.message }, 500);
  }
});

