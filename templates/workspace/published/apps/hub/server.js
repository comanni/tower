/**
 * Publishing Hub v2 — Infrastructure Management Dashboard
 *
 * 앱/사이트를 등록하면 nginx config + systemd service를 자동 생성하고,
 * 대시보드에서 start/stop/restart/logs까지 원클릭으로 관리.
 *
 * API:
 *   GET  /hub/                        → Dashboard UI
 *   GET  /hub/api/manifest            → manifest.json
 *   GET  /hub/api/health              → health check (apps + sites)
 *   GET  /hub/api/stats               → nginx traffic stats
 *   POST /hub/api/apps                → 앱 등록 + nginx + systemd 배포
 *   POST /hub/api/sites               → 사이트 등록
 *   DELETE /hub/api/apps/:name        → 앱 제거
 *   DELETE /hub/api/sites/:name       → 사이트 제거
 *   POST /hub/api/apps/:name/start    → systemd start
 *   POST /hub/api/apps/:name/stop     → systemd stop
 *   POST /hub/api/apps/:name/restart  → systemd restart
 *   GET  /hub/api/apps/:name/logs     → journalctl logs
 *   POST /hub/api/sync                → manifest ↔ infra 동기화
 *   POST /hub/api/nginx/reload        → nginx 강제 reload
 */

const http = require('http');
const fs = require('fs');
const path = require('path');
const os = require('os');
const { exec } = require('child_process');

// ════════════════════════════════════════════════════════════
// Constants
// ════════════════════════════════════════════════════════════

const PORT = parseInt(process.env.HUB_PORT || '32400', 10);
const PUBLISHED_DIR = process.env.PUBLISHED_DIR || path.join(os.homedir(), 'workspace/published');
const MANIFEST_PATH = path.join(PUBLISHED_DIR, 'manifest.json');
const SERVICE_PREFIX = 'pub-';
const USER = process.env.HUB_USER || os.userInfo().username;

// Domain and nginx conf path are derived from manifest at runtime
function getNginxConfPath() {
  try {
    const m = JSON.parse(fs.readFileSync(MANIFEST_PATH, 'utf-8'));
    const domain = m.domain || 'localhost';
    return `/etc/nginx/sites-available/${domain}`;
  } catch { return '/etc/nginx/sites-available/pub-hub'; }
}

// ════════════════════════════════════════════════════════════
// Helpers
// ════════════════════════════════════════════════════════════

function readManifest() {
  return JSON.parse(fs.readFileSync(MANIFEST_PATH, 'utf-8'));
}

function writeManifest(data) {
  data.updated_at = new Date().toISOString();
  fs.writeFileSync(MANIFEST_PATH, JSON.stringify(data, null, 2) + '\n');
}

function json(res, data, status = 200) {
  res.writeHead(status, { 'Content-Type': 'application/json; charset=utf-8' });
  res.end(JSON.stringify(data));
}

function readBody(req) {
  return new Promise((resolve) => {
    let body = '';
    req.on('data', c => body += c);
    req.on('end', () => {
      try { resolve(JSON.parse(body)); }
      catch { resolve(null); }
    });
  });
}

function run(cmd) {
  return new Promise((resolve) => {
    exec(cmd, { timeout: 15000 }, (err, stdout, stderr) => {
      resolve({
        ok: !err,
        stdout: (stdout || '').trim(),
        stderr: (stderr || '').trim(),
        code: err?.code,
      });
    });
  });
}

function svcName(appName) {
  return `${SERVICE_PREFIX}${appName}.service`;
}

// ════════════════════════════════════════════════════════════
// Nginx Config Generation
// ════════════════════════════════════════════════════════════

function generateAppLocation(app) {
  if (app.name === 'hub') return ''; // hub is hardcoded in template

  const upstream = `http://127.0.0.1:${app.port}`;
  const proxyHeaders = `
        proxy_http_version 1.1;
        proxy_set_header Upgrade $http_upgrade;
        proxy_set_header Connection "upgrade";
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;`;

  if (app.access === 'private') {
    return `
    # ── ${app.name} (login required) ──
    location ${app.path} {
        auth_request /internal/auth;
        error_page 401 = @${app.name.replace(/-/g, '_')}_unauth;

        proxy_pass ${upstream}/;${proxyHeaders}
    }

    location @${app.name.replace(/-/g, '_')}_unauth {
        return 302 /?login=required;
    }
`;
  }

  return `
    # ── ${app.name} (public) ──
    location ${app.path} {
        proxy_pass ${upstream}/;${proxyHeaders}
    }
`;
}

function generateNginxConfig(manifest) {
  const appBlocks = manifest.apps
    .filter(a => a.name !== 'hub')
    .map(generateAppLocation)
    .join('');

  const extraBlocks = (manifest.extra_locations || [])
    .map(loc => `
    # ── ${loc.comment} ──
    ${loc.config}
`)
    .join('');

  const domain = manifest.domain || 'localhost';
  return `# ═══════════════════════════════════════════════════════════════
# ${domain} — Auto-generated by Publishing Hub v2
# Last updated: ${new Date().toISOString()}
# DO NOT EDIT MANUALLY — use /hub/ dashboard or API
# ═══════════════════════════════════════════════════════════════

server {
    listen 80;
    server_name ${domain};

    # ── Auth subrequest (Tower JWT check) ──
    location = /internal/auth {
        internal;
        proxy_pass http://localhost:32355/api/auth/check;
        proxy_pass_request_body off;
        proxy_set_header Content-Length "";
        proxy_set_header Authorization $http_authorization;
    }

    # ── Publishing Hub (login required) ──
    location /hub/ {
        auth_request /internal/auth;
        error_page 401 = @hub_unauthorized;

        proxy_pass http://127.0.0.1:${PORT}/hub/;
        proxy_http_version 1.1;
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
    }

    location @hub_unauthorized {
        return 302 /?login=required;
    }
${appBlocks}${extraBlocks}
    # ── Static Sites (public) ──
    location /sites/ {
        alias ${PUBLISHED_DIR}/sites/;
        autoindex on;
        index index.html;

        types {
            text/html                html htm;
            text/css                 css;
            application/javascript   js mjs;
            application/json         json;
            image/png                png;
            image/jpeg               jpg jpeg;
            image/gif                gif;
            image/svg+xml            svg;
            application/pdf          pdf;
            font/woff2               woff2;
            font/woff                woff;
            application/octet-stream ttf otf;
        }
    }

    # ── Tower (default — must be last) ──
    location / {
        proxy_pass http://localhost:32354;
        proxy_http_version 1.1;
        proxy_set_header Upgrade $http_upgrade;
        proxy_set_header Connection "upgrade";
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto $scheme;
    }
}
`;
}

async function deployNginx(manifest) {
  const config = generateNginxConfig(manifest);
  const tmpPath = '/tmp/pub-hub-nginx.conf';
  const nginxConfPath = getNginxConfPath();
  fs.writeFileSync(tmpPath, config);

  // Test before applying
  const test = await run(`sudo cp ${tmpPath} ${nginxConfPath} && sudo nginx -t`);
  if (!test.ok) {
    // Restore backup
    await run(`sudo cp /tmp/pub-hub-nginx.bak ${nginxConfPath} 2>/dev/null`);
    return { ok: false, error: 'nginx config test failed', detail: test.stderr };
  }

  const reload = await run('sudo nginx -s reload');
  return { ok: reload.ok, config };
}

// ════════════════════════════════════════════════════════════
// Systemd Service Management
// ════════════════════════════════════════════════════════════

function generateServiceFile(app) {
  const workDir = app.working_dir || path.join(PUBLISHED_DIR, 'apps', app.name);
  const execStart = app.exec || (app.runtime === 'python'
    ? `/usr/bin/python3 ${workDir}/server.py`
    : `/usr/bin/node ${workDir}/server.js`);

  return `[Unit]
Description=${app.description || app.name}
After=network.target

[Service]
Type=simple
User=${USER}
WorkingDirectory=${workDir}
ExecStart=${execStart}
Restart=on-failure
RestartSec=5
Environment=NODE_ENV=production

[Install]
WantedBy=multi-user.target
`;
}

async function installService(app) {
  const name = svcName(app.name);
  const content = generateServiceFile(app);
  const tmpPath = `/tmp/${name}`;
  fs.writeFileSync(tmpPath, content);

  const result = await run(
    `sudo cp ${tmpPath} /etc/systemd/system/${name} && sudo systemctl daemon-reload && sudo systemctl enable ${name}`
  );
  return result;
}

async function removeService(appName) {
  const name = svcName(appName);
  await run(`sudo systemctl stop ${name} 2>/dev/null`);
  await run(`sudo systemctl disable ${name} 2>/dev/null`);
  await run(`sudo rm /etc/systemd/system/${name} 2>/dev/null && sudo systemctl daemon-reload`);
  return { ok: true };
}

async function serviceAction(appName, action) {
  const name = svcName(appName);
  if (action === 'status') {
    const result = await run(`systemctl is-active ${name} 2>/dev/null`);
    return { status: result.stdout || 'unknown' };
  }
  const result = await run(`sudo systemctl ${action} ${name}`);
  return { ok: result.ok, error: result.ok ? null : result.stderr };
}

async function getServiceLogs(appName, lines = 80) {
  const name = svcName(appName);
  const result = await run(`journalctl -u ${name} -n ${lines} --no-pager 2>/dev/null`);
  return result.stdout || 'No logs available';
}

// ════════════════════════════════════════════════════════════
// Full Deploy: register app → install service → deploy nginx
// ════════════════════════════════════════════════════════════

async function fullDeploy(app, manifest) {
  const steps = [];

  // 1. Install systemd service (if managed)
  if (app.managed !== false) {
    const svc = await installService(app);
    steps.push({ step: 'systemd-install', ...svc });

    // Start service
    const start = await serviceAction(app.name, 'start');
    steps.push({ step: 'systemd-start', ...start });
  }

  // 2. Deploy nginx config
  const nginx = await deployNginx(manifest);
  steps.push({ step: 'nginx-deploy', ok: nginx.ok, error: nginx.error });

  return { ok: steps.every(s => s.ok !== false), steps };
}

// ════════════════════════════════════════════════════════════
// Sync: ensure manifest matches infra reality
// ════════════════════════════════════════════════════════════

async function syncAll() {
  const manifest = readManifest();
  const results = [];

  for (const app of manifest.apps) {
    if (app.managed === false) continue;

    // Check if service exists
    const exists = await run(`systemctl cat ${svcName(app.name)} 2>/dev/null`);
    if (!exists.ok) {
      const install = await installService(app);
      results.push({ app: app.name, action: 'install-service', ...install });
    }

    // Check if running
    const status = await serviceAction(app.name, 'status');
    if (status.status !== 'active') {
      const start = await serviceAction(app.name, 'start');
      results.push({ app: app.name, action: 'start', ...start });
    }
  }

  // Redeploy nginx
  const nginx = await deployNginx(manifest);
  results.push({ action: 'nginx-deploy', ...nginx });

  // Clean up empty site dirs
  const sitesDir = path.join(PUBLISHED_DIR, 'sites');
  if (fs.existsSync(sitesDir)) {
    for (const dir of fs.readdirSync(sitesDir)) {
      const full = path.join(sitesDir, dir);
      if (!fs.statSync(full).isDirectory()) continue;
      const files = fs.readdirSync(full).filter(f => !f.startsWith('.'));
      const inManifest = manifest.sites.some(s => s.name === dir);
      if (files.length === 0 && !inManifest) {
        fs.rmdirSync(full);
        results.push({ action: 'cleanup', dir, removed: true });
      }
    }
  }

  return results;
}

