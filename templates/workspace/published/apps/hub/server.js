// Health Checks
// ════════════════════════════════════════════════════════════

function checkAppHealth(app) {
  return new Promise((resolve) => {
    if (!app.port) return resolve({ ...app, status: 'no-port', service: 'unknown' });

    // Also check systemd status
    const svc = svcName(app.name);
    exec(`systemctl is-active ${svc} 2>/dev/null`, (_, stdout) => {
      const serviceStatus = (stdout || '').trim() || 'unmanaged';

      const req = http.get(`http://localhost:${app.port}${app.health_path || '/'}`, { timeout: 3000 }, (res) => {
        resolve({ ...app, status: res.statusCode < 500 ? 'up' : 'down', statusCode: res.statusCode, service: serviceStatus });
      });
      req.on('error', () => resolve({ ...app, status: 'down', service: serviceStatus }));
      req.on('timeout', () => { req.destroy(); resolve({ ...app, status: 'timeout', service: serviceStatus }); });
    });
  });
}

function checkSiteExists(site) {
  const dir = path.join(PUBLISHED_DIR, 'sites', site.name);
  const exists = fs.existsSync(dir);
  const files = exists ? fs.readdirSync(dir).filter(f => !f.startsWith('.')).length : 0;
  return {
    ...site,
    status: exists && files > 0 ? 'live' : (exists ? 'empty' : 'missing'),
    files,
  };
}

function getRecentStats() {
  return new Promise((resolve) => {
    exec(
      'tail -1000 /var/log/nginx/access.log 2>/dev/null | grep -E "/sites/|/hub/|/edge/|/pulse/|/okusystem/" | awk \'{print $7}\' | sed \'s/\\?.*//' + '\' | sort | uniq -c | sort -rn | head -20',
      (err, stdout) => {
        if (err) return resolve([]);
        const lines = stdout.trim().split('\n').filter(Boolean);
        resolve(lines.map(l => {
          const m = l.trim().match(/^(\d+)\s+(.+)/);
          return m ? { hits: parseInt(m[1]), path: m[2] } : null;
        }).filter(Boolean));
      }
    );
  });
}

